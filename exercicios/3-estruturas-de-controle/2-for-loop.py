# Crie uma lista
lista=["samuel","mari","lucilene","renato"]
# Crie um for loop para imprimir cada elemento dessa lista

for nome in lista:
  print(nome)

# Crie um objeto iterável utilizando a função range()
meu_range=range(0,12,2)

# Use esse objeto iterável para criar um for loop que imprima na tela a frase "Estou aprendendo uma linguagem de programação."
for i in meu_range:
  print(f"{i} - Estou aprendendo uma linguagem de programação.")

for a in lista:
  print(f"{a} - Estou aprendendo uma linguagem de programação.")